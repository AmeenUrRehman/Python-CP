# Indentation helps in indicating a block of code

name = "Same"
if name == "Same":
    print("Name is Same")
    ## --> the space before the print is called indentation basically the whitespace.
else:
    print("Name is NotSame")

name = "NotSame"
if name == "Same":
    print("Name is Same")
else:
    print("Name is NotSame")